import turtle as tur

def drawStar (colour, pen, x, y):
    tur.fillcolor(colour)
    tur.pencolor(pen)
    tur.penup()
    tur.goto(x, y)
    tur.pendown()
    tur.width(2)
    tur.begin_fill()
    for i in range(5):
        tur.forward(100)
        tur.right(144)
    tur.end_fill()

